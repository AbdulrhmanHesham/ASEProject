    private void AppointmentjButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AppointmentjButton1ActionPerformed
        // TODO add your handling code here:
        
        String SelectedSpecialization = (String) SpecializationComboBox.getSelectedItem();
        String AvailableDoc = (String) AVDocComboBox2.getSelectedItem();
        String AvailableDate_time = (String) AVdateComboBox1.getSelectedItem();
        String PatientsName = PatientsName_Text.getText();
        String PatientsSSN = PatientsSSN_Text.getText();
        
        if(SelectedSpecialization.isEmpty()){
            JOptionPane.showMessageDialog(this, "Please Choose the Specialization");
            return;
        }
        if(AvailableDoc.isEmpty()){
            JOptionPane.showMessageDialog(this, "Please Choose a Doctor");
            return;
        }
        if(AvailableDate_time.isEmpty()){
            JOptionPane.showMessageDialog(this, "Please Pick Date and Time");
            return;
        }
        if(PatientsName.isEmpty()){
            JOptionPane.showMessageDialog(this, "Please Enter Your Name");
            return;
        }
         if(PatientsSSN.isEmpty()){
            JOptionPane.showMessageDialog(this, "Please Enter your SSN");
            return;
        }
        if(PatientsSSN.length()> 14 || PatientsSSN.length()< 14){
            JOptionPane.showMessageDialog(this, "The SSN You Entered Is Longer Than 14 Digits Or Shorter Than 14 Digits!");
            
        }
        if(!PatientsSSN.matches("\\d+")){
            JOptionPane.showMessageDialog(this, "Please Enter Digits");
        }
        
        MakeAppointment(PatientsName, PatientsSSN, SelectedSpecialization,AvailableDoc, AvailableDate_time);
        
    }//GEN-LAST:event_AppointmentjButton1ActionPerformed

