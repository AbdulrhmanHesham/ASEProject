    public void MakeAppointment(String Patientname, String PatientSSN, String SelectedSpecialization, String AvailableDoc, String AvailableDate_time){
        
        Connection con = null;
        Statement stmt = null;
        String query = null;
        
        try{
            con = DriverManager.getConnection(url,user, passwordsql);
            stmt = con.createStatement();
            query = "INSERT INTO appointments VALUES ('"+ Patientname +"','"+ AvailableDoc +"','"+ AvailableDate_time +"','"+ PatientSSN +"')";
            
            int rowsInserted = stmt.executeUpdate(query);
            
            if (rowsInserted > 0) {
                JOptionPane.showMessageDialog(null, "Appointment booked successfully!");
            } else {
                JOptionPane.showMessageDialog(null, "Failed to book appointment.");
            }
            
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, "Error: " + ex.getMessage());
        }finally
        {
            if(con !=null){try{con.close();}catch(Exception ex){}}
            if(stmt !=null){try{stmt.close();}catch(Exception ex){}}
         }
    }
    
    public void SelectedSpecialization(String SelectedSpecialization){
        
        AVDocComboBox2.removeAllItems();
        
        Connection con = null;
        Statement stmt = null;
        ResultSet result = null;
        String query = null;
        
        try{
            
             con = DriverManager.getConnection(url,user, passwordsql);
             stmt = con.createStatement();
             query = "SELECT first_name, last_name FROM doctors WHERE specialization = '"+SelectedSpecialization+"'";
             result = stmt.executeQuery(query);
             
             while(result.next()){
                 
                 String doctorName = result.getString("first_name") +" "+ result.getString("last_name");
                 AVDocComboBox2.addItem(doctorName);
                 
                 AVdateComboBox1.removeAllItems();
                 
                 AVdateComboBox1.addItem("2025-05-29 10:00:00");
                 AVdateComboBox1.addItem("2025-05-30 11:30:00");
                 AVdateComboBox1.addItem("2025-05-27 13:00:00"); 
                 AVdateComboBox1.addItem("2025-05-24 14:30:00"); 
                 AVdateComboBox1.addItem("2025-05-25 16:00:00"); 
             }
            
            
        }catch(Exception ex){
             JOptionPane.showMessageDialog(this, "Error loading doctors: " + ex.getMessage());
        } finally
        {
            if(con !=null){try{con.close();}catch(Exception ex){}}
            if(stmt !=null){try{stmt.close();}catch(Exception ex){}}
            if(result !=null){try{result.close();}catch(Exception ex){}}
         }
    }

